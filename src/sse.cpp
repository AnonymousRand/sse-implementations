#include "sse.h"

////////////////////////////////////////////////////////////////////////////////
// SseClient
////////////////////////////////////////////////////////////////////////////////

SseClient::SseClient(Db db) {
    this->db = db;
    if (this->db.empty()) {
        std::cerr << "Error: `db` passed to `SseClient.SseClient()` is empty :/" << std::endl;
        exit(EXIT_FAILURE);
    }
}

